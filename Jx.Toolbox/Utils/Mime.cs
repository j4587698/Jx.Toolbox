        public static string DefaultMime = "application/octet-stream";

        /// <summary>
        /// 根据扩展名获取Mime
        /// </summary>
        /// <param name="extension"></param>
        /// <returns></returns>
        public static string GetMimeFromExtension(string extension)
        {
            if (!extension.StartsWith("."))
            {
                extension = "." + extension;
            }

            return MimeItems.Find(x => x.Extension == extension)?.Mime ?? DefaultMime;
        }

        /// <summary>
        /// 根据扩展名获取大类
        /// </summary>
        /// <param name="extension"></param>
        /// <returns></returns>
        public static string GetTypeFormExtension(string extension)
        {
            var mime = GetMimeFromExtension(extension);
            return mime.Split('/')[0];
        }

        /// <summary>
        /// 根据Mime获取扩展名，如果未找到，返回null
        /// </summary>
        /// <param name="mime"></param>
        /// <returns></returns>
        public static string GetExtensionFromMime(string mime)
        {
            return MimeItems.Find(x => x.Mime == mime)?.Extension;
        }

